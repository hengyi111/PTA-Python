# 第2章-7 产生每位数字相同的n位数 (30分)
# 读入2个正整数A和B，1<=A<=9, 1<=B<=10,产生数字AA...A,一共B个A

# 输入格式:
# 在一行中输入A和B。

# 输出格式:
# 在一行中输出整数AA...A,一共B个A

# 输入样例1:
# 在这里给出一组输入。例如：

#   1,  5
# 输出样例1:
# 在这里给出相应的输出。例如：

# 11111
# 输入样例2:
# 在这里给出一组输入。例如：

#         3  ,4
# 输出样例2:
# 在这里给出相应的输出。例如：

# 3333

A, B = input().split(",")
A = A.strip()
B = B.strip()
B = int(B)
out = int(A * B)
print(out)
